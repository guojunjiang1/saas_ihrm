    //生成员工详细信息的PDF报表
    @RequestMapping(value = "/a/{id}/pdf",method = RequestMethod.GET)
    public void pdf(@PathVariable("id") String id){
        FileInputStream inputStream=null;
        ServletOutputStream outputStream=null;
        try{
            //一：用JasperSoftStudio工具构建出模板
            //二：加载模板(jasper文件)
            Resource resource=new ClassPathResource("templates/profile.jasper");
            inputStream=new FileInputStream(resource.getFile());

            //三：准备数据。。
            Map map=new HashMap();
            UserCompanyPersonal user1 = userCompanyPersonalService.findById(id);//用户详情数据
            UserCompanyJobs user2 = userCompanyJobsService.findById(id);//用户工作数据
            Map<String, Object> map1 = BeanMapUtils.beanToMap(user1);
            Map<String, Object> map2 = BeanMapUtils.beanToMap(user2);
            map.putAll(map1);
            map.putAll(map2);
            String staffPhoto="http://q9bn881u7.bkt.clouddn.com/"+id;
            map.put("staffPhoto",staffPhoto);//用户头像地址

            //四：在模板中填充数据生成JasperPrint
            JasperPrint jasperPrint= JasperFillManager.fillReport(inputStream,map,new JREmptyDataSource());
            //五：以PDF形式输出
            outputStream = response.getOutputStream();
            JasperExportManager.exportReportToPdfStream(jasperPrint,outputStream);
        }catch (Exception e){
            e.printStackTrace();
            ExceptionCast.cast(ResultCode.FAIL);
        }finally {
            //六：关闭流
            if (inputStream!=null) {
                try {
                    inputStream.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            if (outputStream!=null) {
                try {
                    outputStream.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
    }

